package com.java.Program;

public class ArrayForEachLoop {
	public static void main(String[] args) {
	// create an array 
	int[] age = {25,35,45,55};
	
	// Loop through the array 
	// using for loop 
	
	System.out.println("Using For Each Loop ");
	for (int i : age) {
		System.out.println("\t"+i);
	}
	
	}
	
}
