package com.java.Program;

// Program to display number from 1 to 5
public class DoWhileSimpleProgram {
	public static void main(String[] args) {
		// declare variables
		int i=1, n=5;
		// Do While loop from 1 to 5
		do {
			System.out.println(i);
			i++;
		} while (i<=n);
		
		
	}
}
