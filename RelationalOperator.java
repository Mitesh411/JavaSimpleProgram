package com.java.Program;

public class RelationalOperator {
	public static void main(String[] args) {
		// create variables
		int a = 7, b = 11;

		// value of a and b
		System.out.println("a is " + a + "and b is " + b);

		// == Operator   False
		System.out.println(a == b);

		// != Operator   True
		System.out.println(a != b);

		// > Operator   True
		System.out.println(a > b);

		// < Operator     False
		System.out.println(a < b);

		// >= Operator     True
		System.out.println(a >= b);

		// <= Operator       True     
		System.out.println(a <= b);

	}

}
