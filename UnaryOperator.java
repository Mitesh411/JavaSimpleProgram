package com.java.Program;

public class UnaryOperator {
	public static void main(String[] args) {
		// declare variables
		int a = 12, b=12;
		int result1, result2;
		
		//Original Value 
		System.out.println("\n Value of a :- "+a);
		
		//increment Operator
		result1 = ++a;
		System.out.println("\n After Increment :- "+result1);
		
		
		System.out.println("\n Value of b - "+b);
		//Decrement Operator 
		result2 = --b;
		System.out.println("\n After Decrement :- "+result2);
			
	}
	
	
}
