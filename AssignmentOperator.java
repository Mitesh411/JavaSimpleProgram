package com.java.Program;

public class AssignmentOperator {

	public static void main(String args[]) {
		// create Variables
		int a = 4;
		int var;

		// assign value using =
		var = a;
		System.out.println("\n var using =: " + var);

		// assign value using +=
		var += a;
		System.out.println("\n var using += " + var);

		// assign value using -=
		var -= a;
		System.out.println("\n var using -= " + var);

		
		// assign value using *=
		var *= a;
		System.out.println("\n var using *= " + var);

		// assign value using /=
		var /= a;
		System.out.println("\n var using /= " + var);

		// assign value using %=
		var %= a;
		System.out.println("\n var using %= " + var);

	}

}
