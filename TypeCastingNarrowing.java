package additionalTopic;

public class TypeCastingNarrowing {
	public static void main(String[] args) {
		
		//This is Known as Explicit Type Casting 
		
		//Create a double Type Variable 
		double num = 10.99;
		System.out.println("The Double Number :- "+num);
		
		//Convert into int type 
		int data = (int) num;
		System.out.println("The Integer Value :- "+data);
		
		
	}
}
