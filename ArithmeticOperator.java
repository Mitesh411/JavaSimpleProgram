package com.java.Program;

public class ArithmeticOperator {
	public static void main (String args[]) {
		
		// Integer Declare Variable
		int a=10, b=12, c;
		
		// Addition Operator 
		System.out.println("\n Addition of Two numbers \n a + b = "+(c=a+b));
		
		// Subtraction Operator 
		System.out.println("\n Subtraction of Two Number \n a - b = "+(c=a-b));
		
		// Division Operator
		System.out.println("\n Division of Two Number \n a / b = "+(c=b/a));
		
		// Multiplication Operator 
		System.out.println("\n Multiplication of Two Number \n a * b = "+(c=a*b));
		
		// Modulo Operator
		System.out.println("\n Multiplication of Two Number \n b % a = "+(c=b%a));
	}
}
